#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*ptr;

	ptr = (char *)ft_calloc(ft_strlen(str) + 1, sizeof(char));
	if (!ptr)
	{
		return (NULL);
	}
	i = 0;
	while (str[i])
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
